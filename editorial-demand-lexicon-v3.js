  function buildEntries() {
    const seen = new Set();
    return RAW_ENTRIES.map(([answer, clue, lexicalQuality], index) => {
      const normalized = normalize(answer);
      if (!/^[А-Я]+$/.test(normalized)) {
        throw new Error(`Invalid demand-lexicon answer at ${index}: ${answer}`);
      }
      if (seen.has(normalized)) {
        throw new Error(`Duplicate demand-lexicon answer: ${normalized}`);
      }
      seen.add(normalized);
      return Object.freeze({
        id: `editorial-demand-v3:${normalized}`,
        answer: normalized,
        clue: String(clue || "").trim(),
        hasExactClue: true,
        weakFill: false,
        lexicalQuality: Number(lexicalQuality || 80),
        lexicalSource: "editorial-demand-lexicon-v3",
      });
    });
  }

  const ENTRIES = Object.freeze(buildEntries());

  function extendPool(result) {
    if (!result || !Array.isArray(result.pool)) return result;
    const existing = new Set(result.pool.map((entry) => normalize(entry?.answer)));
    const added = [];
    let skippedDuplicates = 0;

    for (const entry of ENTRIES) {
      if (existing.has(entry.answer)) {
        skippedDuplicates += 1;
        continue;
      }
      existing.add(entry.answer);
      added.push({ ...entry });
    }

    if (added.length) result.pool.push(...added);
    const byLength = {};
    for (const entry of added) {
      byLength[entry.answer.length] = (byLength[entry.answer.length] || 0) + 1;
    }
    result.constructionV2 = {
      ...(result.constructionV2 || {}),
      editorialDemandLexicon: {
        mode: "repair-only-demand-lexicon-v3",
        availableEntries: ENTRIES.length,
        addedEntries: added.length,
        skippedDuplicateEntries: skippedDuplicates,
        addedByLength: byLength,
        sourceRun: DEMAND_METADATA.sourceRun,
        sourceArtifact: DEMAND_METADATA.sourceArtifact,
        sourcePatterns: DEMAND_METADATA.sourcePatterns,
        coveredPatterns: DEMAND_METADATA.coveredPatterns,
        uncoveredPatterns: [...DEMAND_METADATA.uncoveredPatterns],
      },
    };
    return result;
  }

  window.ScanwordEditorialDemandLexiconV3 = Object.freeze({
    entries: ENTRIES,
    metadata: DEMAND_METADATA,
    extendPool,
    normalize,
  });
